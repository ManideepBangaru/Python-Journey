# Variable declarations

A = 12      # integer
B = 12.89   # Float
C = True    # Boolean
D = 'books' # String
print(type(A))
print(type(B))
print(type(C))
print(type(D))

# Data Structures
ds1 = (1,2,3,4) # tuple # immutable
ds2 = [1,2,3,4] # list  # mutable
ds3 = {"Name":["Manideep","Shravani"],"Age":[28,25]} # dict
print(type(ds1))
print(type(ds2))
print(type(ds3))

# control flow statements
for i in ds2:
    print(i)

for i in range(len(ds2)):
    print(ds2[i])

